package com.example.techvalutproject14_12.Helper_Class;


public class HelperTeacherClass {
    String T_name, T_enrolment, T_department, T_email, T_password, T_conformPass;

    public HelperTeacherClass(String t_name, String t_enrolment, String t_department, String t_email, String t_password, String t_conformPass) {
        T_name = t_name;
        T_enrolment = t_enrolment;
        T_department = t_department;
        T_email = t_email;
        T_password = t_password;
        T_conformPass = t_conformPass;
    }

    public String getT_name() {
        return T_name;
    }

    public void setT_name(String t_name) {
        T_name = t_name;
    }

    public String getT_enrolment() {
        return T_enrolment;
    }

    public void setT_enrolment(String t_enrolment) {
        T_enrolment = t_enrolment;
    }

    public String getT_department() {
        return T_department;
    }

    public void setT_department(String t_department) {
        T_department = t_department;
    }

    public String getT_email() {
        return T_email;
    }

    public void setT_email(String t_email) {
        T_email = t_email;
    }

    public String getT_password() {
        return T_password;
    }

    public void setT_password(String t_password) {
        T_password = t_password;
    }

    public String getT_conformPass() {
        return T_conformPass;
    }

    public void setT_conformPass(String t_conformPass) {
        T_conformPass = t_conformPass;
    }

    public HelperTeacherClass() {
    }
}