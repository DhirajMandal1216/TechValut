package com.example.techvalutproject14_12.Helper_Class;

public class HelperClass {
    String S_name, S_enrolment, S_course, S_email, S_password, S_conformPass;

    public HelperClass(String s_name, String s_enrolment, String s_course, String s_email, String s_password, String s_conformPass) {
        S_name = s_name;
        S_enrolment = s_enrolment;
        S_course = s_course;
        S_email = s_email;
        S_password = s_password;
        S_conformPass = s_conformPass;
    }

    public String getS_name() {
        return S_name;
    }

    public void setS_name(String s_name) {
        S_name = s_name;
    }

    public String getS_enrolment() {
        return S_enrolment;
    }

    public void setS_enrolment(String s_enrolment) {
        S_enrolment = s_enrolment;
    }

    public String getS_course() {
        return S_course;
    }

    public void setS_course(String s_course) {
        S_course = s_course;
    }

    public String getS_email() {
        return S_email;
    }

    public void setS_email(String s_email) {
        S_email = s_email;
    }

    public String getS_password() {
        return S_password;
    }

    public void setS_password(String s_password) {
        S_password = s_password;
    }

    public String getS_conformPass() {
        return S_conformPass;
    }

    public void setS_conformPass(String s_conformPass) {
        S_conformPass = s_conformPass;
    }

    public HelperClass() {
    }
}